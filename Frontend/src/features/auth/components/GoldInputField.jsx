import { useState } from 'react'

/* ── Shared Gold Input Field ── */
export default function GoldInputField({ label, name, type = 'text', value, onChange, placeholder, icon, rightElement }) {
  const [focused, setFocused] = useState(false)
  return (
    <div style={{ display: 'flex', flexDirection: 'column', gap: '0.375rem' }}>
      <label
        htmlFor={name}
        style={{
          fontFamily: "'Inter', sans-serif",
          fontSize: '0.6875rem', fontWeight: 700,
          letterSpacing: '0.08em', textTransform: 'uppercase',
          color: focused ? '#C9A84C' : '#7E7665',
          transition: 'color 0.2s',
        }}
      >
        {label}
      </label>
      <div
        style={{
          display: 'flex', alignItems: 'center', gap: '0.75rem',
          paddingBottom: '0.75rem',
          borderBottom: `2px solid ${focused ? '#C9A84C' : 'rgba(208,197,178,0.7)'}`,
          transition: 'border-color 0.2s',
        }}
      >
        {icon && (
          <span style={{ color: focused ? '#C9A84C' : '#9E9488', transition: 'color 0.2s', display: 'flex' }}>
            {icon}
          </span>
        )}
        <input
          id={name}
          name={name}
          type={type}
          value={value}
          onChange={onChange}
          placeholder={placeholder}
          onFocus={() => setFocused(true)}
          onBlur={() => setFocused(false)}
          style={{
            flex: 1, background: 'transparent', border: 'none', outline: 'none',
            color: '#231A03', fontFamily: "'Inter', sans-serif",
            fontSize: '0.9375rem', caretColor: '#C9A84C',
          }}
        />
        {rightElement}
      </div>
    </div>
  )
}

